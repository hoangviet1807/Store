export const changeColor = (color) => {
  if (color === "Đỏ") {
    return "red";
  }
  if (color === "Trắng") {
    return "white";
  }
  if (color === "Đen") {
    return "black";
  }
  if (color === "Hồng") {
    return "pink";
  }
  if (color === "Nâu") {
    return "brown";
  }
  if (color === "Cam") {
    return "orange";
  }
};
